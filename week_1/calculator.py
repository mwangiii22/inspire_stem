# simple calculator program



f_number = input("Enter the first number :")
s_number = input("Enter the second number :")

print(int(f_number) + int(s_number))
print(int(f_number) - int(s_number))
print(int(f_number) * int(s_number))
print(int(f_number) / int(s_number))
print(int(f_number) % int(s_number))